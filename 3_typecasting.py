# In python if we take input the default datatype of input is string then we use typecasting
# to change the datatype of the input we take from the user.

name = input('Enter your name : ')
age = int(input('Enter your age : '))
salary = float(input('Enter your salary : '))

print(name , type(name))
print(age , type(age))
print(salary , type(salary))




