#       Arithmetic Operators
#       Used to perform mathematical operations.

#       + : Addition
#       - : Subtraction
#       * : Multiplication
#       / : Division
#       % : Modulus (remainder)
#       ** : Exponentiation (power)
#       // : Floor division (division that rounds down to the nearest integer)


num1 = 20 
num2 = 5

ans = num1+num2
print('After adding : ', ans)

ans = num1-num2
print('After subtracting : ', ans)

ans = num1*num2
print('After multiplication : ', ans)

ans = num1/num2
print('After division : ', ans)

ans = num1**num2  # num1^num2  power
print('After Exponentiation : ', ans)

ans = num1//num2  # also called integer division
print('After Floor division : ', ans)


